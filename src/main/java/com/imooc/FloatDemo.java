package com.imooc;

public class FloatDemo {
    public static void main(String[] args) {
        //定义一个浮点型数
        float f=1.234f;
        System.out.println("f:"+f);
        //定义一个double类型
        double d=1.23;
        System.out.println("d:"+d);
        //整型赋给double
        double d1=12;
        System.out.println("d1:"+d1);
        //将变量d赋给d2
        double d2=d;
        System.out.println("d2:"+d2);

    }
}
